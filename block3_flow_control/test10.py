# What do you expect the output of this following program:
i = 0
while i > 3:
    i += 1
    print("Yes")
else:
    i -= 1
    print("No")
# Answer: No
# Explanation: The while condition is false as i not greater than 3, hence the
# else block is executed. The increments and decrements are not going to
# make any difference in the current program
